package com.rdkm.tdkservice.repository;

import java.util.List;
import java.util.UUID;

import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;

import com.rdkm.tdkservice.model.ScriptTestSuite;
import com.rdkm.tdkservice.model.TestSuite;

import jakarta.transaction.Transactional;

/**
 * Repository interface for script test suite
 */
@Repository
public interface ScriptTestSuiteRepository extends JpaRepository<ScriptTestSuite, UUID> {

	/**
	 * This method is used to delete all the test suite mapping for a script.
	 * 
	 * @param testSuite
	 */
	@Transactional
	void deleteAllByTestSuite(TestSuite testSuite);

	/**
	 * This method is used to find all the test suite mapping for a script
	 * 
	 * @param testSuite - the test suite
	 * @return the list of test suite mapping
	 */
	List<ScriptTestSuite> findAllByTestSuite(TestSuite testSuite);

	/**
	 * This method is used to find all the test suite mapping for a script and order
	 * by script order
	 * 
	 * @param testSuite - the test suite
	 * @return the list of test suite mapping
	 */
	List<ScriptTestSuite> findByTestSuiteOrderByScriptOrderAsc(TestSuite testSuite);
}
