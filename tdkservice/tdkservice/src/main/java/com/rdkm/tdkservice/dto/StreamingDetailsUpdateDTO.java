package com.rdkm.tdkservice.dto;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonInclude;

import jakarta.validation.constraints.NotBlank;
import lombok.Data;

/**
 * Represents the details of a streaming entity.
 */
@Data
@JsonIgnoreProperties(ignoreUnknown = true)
@JsonInclude(JsonInclude.Include.NON_NULL)
public class StreamingDetailsUpdateDTO {

	/**
	 * Represents the unique identifier for the StreamingDetails.
	 */
	@NotBlank(message = "Streaming details id is required")
	private Integer streamingDetailsId;

	/**
	 * Represents the name of the StreamingDetails.
	 */

	private String streamId;

	/**
	 * Represents the stream type of the StreamingDetails.
	 */

	private String streamType;
	/**
	 * Represents the channel type of the StreamingDetails.
	 */

	private String channelType;

	/**
	 * Represents the video type of the StreamingDetails.
	 * 
	 */
	private String videoType;

	/**
	 * Represents the audio type of the StreamingDetails.
	 */

	private String audioType;

}
